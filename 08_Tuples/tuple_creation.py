"""
Tuple is the built in datatype in which store values like list,
tuple is immutable datatype.
"""
tup = (88,72,62,14,93,1,46) #create Tuple using "()" 
print(type(tup)) #o/p: <class 'tuple'>
print(tup) #o/p: (88, 72, 62, 14, 93, 1, 46)

tup2 = ("Siddik",20,"Samarth",21,"Aditya","22")
print(type(tup2)) #o/p: <class 'tuple'>
print(tup2) #o/p: ('Siddik', 20, 'Samarth', 21, 'Aditya', '22')