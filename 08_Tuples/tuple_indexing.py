tuple = ("Joy",72,"Jack",46,"Adam",99,"Rosy",92) #Tuple Indexing Start With 0 

print(tuple[0]) #o/p: ("Joy")
print(tuple[2]) #o/p: ("Jack")
print(tuple[7]) #o/p: ("92")

# tuple[0] = 7 , 'tuple' object does not support item assignment