a = 10 #10 values assign to the a variable
print("Origenal Value: ",a)

a += 1 #addition assignment
print(a)

a -= 5 #subtraction assignment
print(a)

a *= 2 #multiplication assignment
print(a)

a /= 6 #division assignment
print(a)

a %= 5 #modulus assignment
print(a)

a **= 2 #power assignment
print(a)