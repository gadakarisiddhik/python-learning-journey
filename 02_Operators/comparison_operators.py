print(a!=b) #check a not equals to b and return true or false ,o/p: true
print(a>b) #check greater than and return true or false ,o/p: true
print(a>=b) #check greater than equal to and return true or false ,o/p: true
print(a<b) #check less than and return true or false ,o/p: flase
print(a<=b) #check less than equal to and return true or false ,,o/p: flase